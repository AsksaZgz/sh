# Function #####################################################################
function archDelete()
{
  rm /usr/local/java -f
}
function arch32()
{
  echo -e "Architecture 32"
  archDelete
  ln -s /usr/local/jdk1.6.0_20 /usr/local/java
  
}
function arch64()
{
  archDelete
  ln -s /usr/local/jdk1.8.0_92 /usr/local/java
}
################################################################################ 
#
#
#
#
################################################################################ 
# Param ########################################################################
clear
_archType=$1
if [[ $_archType = '' ]]
then
  echo -e "Faltan parametros"
  echo -e "Example: ./arch 32"
  while :
  do
    echo -e "Select architecture 32|64: "
    read _archType
    if [[ $_archType = '32' || $_archType = '64' ]]
    then 
      break
    fi
  done
fi
if [[ $_archType = '32' ]]
then
  arch32
fi
if [[ $_archType = '64' ]]
then
  arch64
fi

